let pi = 3.14159265;
let radius = 10;    // 반지름

console.log("원의 둘레 : " + (2 * pi * radius));
console.log("원의 넓이 : " + (pi * radius * radius));

console.log(`원의 둘레 : ${2 * pi * radius}`);
console.log(`원의 넓이 : ${pi * radius * radius}`);


console.log("반지름 : " + radius + "입니다");   // 방법1
console.log(`반지름 ${radius} 입니다`);         // 방법2